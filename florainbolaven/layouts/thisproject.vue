    <h1 class="text-center">
      Introduction
    </h1>
    <p style="text-align:justify">
      Laos is located in the centre of Indo-Burma biodiversity hotspots which
      was estimated more than 20,000 to 25,000 species of vascular plant and
      half of which are endemic to the hotspot (CEPF, 2012). Lao flora is
      composed of a unique mixture of Himalayan elements, Chinese elements,
      Indochinese elements and Malaysian elements in various proportions (Zhu,
      2017). However, the research on plant diversity are still very
      fragmentary, because the activities of plant taxonomists in the country
      are limited and the number of plant specimens in herbaria is the fewest
      among the Southeast Asian countries:3 specimens/100 km2 in 2007 and 10
      specimens/100 km2 in Middleton et al. 2019). Recently, a checklist of
      vascular plants in Laos has been released and available on the web (Newman
      et al. 2017–present, https://padme.rbge.org.uk/laos/list/), but further
      efforts for botanical inventories and taxonomic studies based on new
      collections are required to accurately know the plant diversity of
      Laos.<br />The Bolaven Plateau is located mostly in Champasak Province,
      and partly in Saravan, Sekong and Attapeu Provinces in southern Laos (Fig.
      1). The area of the plateau is approximately 4,800 km2, mostly ranging
      from 900–1,400 m elevations and surrounded by lowland around 100–200 m
      elevations, with cliffs and steep escarpments, especially on the southern
      and eastern sides. The climate on the plateau is moist and cool all year
      round: average annual temperature is 20.2°C and annual rainfall over 3,800
      mm in Paksong Town at ca 1,290 m elevation (Climate-data.org
      http://climate-data.org/ Accessed 5 Mar. 2020). The edges of the plateau
      are protected mainly for biodiversity conservation at national level (Dong
      Hua Sao National Protected Area) and partly at the district level. Forest
      types are diverse along the altitudinal gradient: lowland evergreen forest
      to lower montane forest at high elevation, with open grassland and sparse
      pine forest. In this project, we focus on the flora of Bolaven Plateau,
      southern Laos to evaluate plant species diversity along an environmental
      gradient of the plateau.
    </p>
